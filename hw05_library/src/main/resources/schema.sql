CREATE TABLE AUTHORS(ID BIGINT GENERATED BY DEFAULT AS IDENTITY,NAME VARCHAR(255),SURNAME VARCHAR(255), PRIMARY KEY (ID));
CREATE TABLE GENRES(ID BIGINT GENERATED BY DEFAULT AS IDENTITY,TITLE VARCHAR(255), PRIMARY KEY (ID));
CREATE TABLE BOOKS(ID BIGINT GENERATED BY DEFAULT AS IDENTITY,NAME VARCHAR(255),AUTHOR_ID BIGINT NOT NULL,GENRE_ID BIGINT NOT NULL,
                   PRIMARY KEY (ID),
                   CONSTRAINT authorConstr FOREIGN KEY (AUTHOR_ID)
                       references AUTHORS ON DELETE CASCADE,
                   CONSTRAINT genreConstr FOREIGN KEY (GENRE_ID)
                       references GENRES ON DELETE CASCADE);